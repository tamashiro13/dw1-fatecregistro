let sub = n1 - n2
document.writeln(`<br>A subtração de ${n1} e ${n2} é igual a ${sub}.`)
//POTÊNCIA
let pot = n1 ** n2
document.writeln(`<br>A potência de ${n1} e ${n2} é igual a ${pot}.`)
//DIVISÃO REAL
let divr = n1 / n2
document.writeln(`<br>A divsão real de ${n1} e ${n2} é igual a ${divr}.`)
//RESTO DA DIVSÃO
let rdiv = n1 % n2
document.writeln(`<br>O resto da divisão de ${n1} e ${n2} é igual a ${rdiv}.`)

