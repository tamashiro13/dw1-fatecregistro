let n1 = Number(window.prompt('Digite um número:'))
let n2 = Number(window.prompt('Digite outro número:'))
const soma = n1 + n2
window.alert(`A soma dos valores é ${soma}`)

// DEMAIS OPERADORES
//MULTIPLICAÇÃO
let mult = n1 * n2
document.writeln(`<br>A multiplicação de ${n1} e ${n2} é igual a ${mult}.`)
